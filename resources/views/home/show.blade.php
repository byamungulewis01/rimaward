                  <p class="dropcap">On 9 August 2016, HMRC released a consultation document to flag some potential tax impacts that a forthcoming change in lease accounting may have on the funding of plant and machinery. From 2019, lessees using IFRS (including FRS 101), as opposed to FRS 102 (UK GAAP), will no longer have to distinguish between finance and operating leases. All leases (except some small value leases or very short leases) will be capitalized on the lessee’s balance sheet at the NPV of the future payments from the day the lease starts. This accounting change will not affect lessors. At the moment, the tax treatment typically depends on whether a lease is a ‘long funding lease’ or not. If it is, then it is the lessee who gets capital allowances (CAs). However, much anti-avoidance legislation is focused on the concept of finance leases and the IFRS change will interfere with that. In its consultation, HMRC has floated four options for addressing the issue. The first is broadly the status quo, with some tinkering. The others contain the more radical suggestion to move towards an accounts-based regime for taxing leases, using a system of debits and credits somewhat akin to the loan relationship regime and which would similarly eliminate the capital/revenue divide. ith such a proposal, including:</p><a class="d-block my-3 ms-4" href="#!">gust-2016/tax-impacts-of-lease-accunting-changes#sthash.J6Ddydx2.dpuf</a>
                  <p>HMRC has identified several issues which should be addressed in conjunction with such a proposal, including:</p>
                  <ol>
                    <li>
                      <p class="mb-2">Can HMRC trust companies to use ‘sensible’ depreciation policies or will there need to be legislation for it?</p>
                    </li>
                    <li>
                      <p class="mb-2">In order to avoid distorting the ‘lease or buy’ decision, the government may be willing to offer an enhanced deduction to lessees that would otherwise have qualified for the AIA.</p>
                    </li>
                    <li>
                      <p class="mb-2">Could HMRC combine an accounts-based regime with giving the lessee an option to claim CAs on the ‘right of use’ asset?</p>
                    </li>
                  </ol>
                  <p>Inevitably, there is a long way to go on these issues. But the eventual outcome is potentially quite far-reaching for UK corporates – for example if HMRC can become comfortable on the issue of ‘sensible’ depreciation policies without a lot of new anti-avoidance legislation.</p>
